void RenderingDeviceVulkan::draw_list_draw(DrawListID p_list, bool p_use_indices, uint32_t p_instances) {

	DrawList *dl = _get_draw_list_ptr(p_list);
	ERR_FAIL_COND(!dl);
	if (!dl->validation.active) {
		ERR_EXPLAIN("Submitted Draw Lists can no longer be modified.");
		ERR_FAIL();
	}

	if (!dl->validation.pipeline_active) {
		ERR_EXPLAINC("No render pipeline was set before attempting to draw.");
		ERR_FAIL();
	}
	if (dl->validation.pipeline_vertex_format != INVALID_ID) {
		//pipeline uses vertices, validate format
		if (dl->validation.vertex_format == INVALID_ID) {
			ERR_EXPLAINC("No vertex array was bound, and render pipeline expects vertices.");
			ERR_FAIL();
		}
		//make sure format is right
		if (dl->validation.pipeline_vertex_format != dl->validation.vertex_format) {
			ERR_EXPLAINC("The vertex format used to create the pipeline does not match the vertex format bound.");
			ERR_FAIL();
		}
		//make sure amount of instances is valid
		if (p_instances > dl->validation.vertex_max_instances_allowed) {
			ERR_EXPLAIN("Amount of instances requested (" + itos(p_instances) + " is larger than the maximum amount suported by the bound vertex array (" + itos(dl->validation.vertex_max_instances_allowed) + ").");
			ERR_FAIL();
		}
	}

	if (dl->validation.pipeline_push_constant_size > 0) {
		//using push constants, check that they were supplied
		if (!dl->validation.pipeline_push_constant_suppplied) {
			ERR_EXPLAIN("The shader in this pipeline requires a push constant to be set before drawing, but it's not present.");
			ERR_FAIL();
		}
	}
	//compare hashes
	if (dl->validation.pipeline_set_hashes.size()) {
		if (dl->validation.pipeline_set_hashes.size() > dl->validation.set_hashes.size()) {
			ERR_EXPLAINC("Render pipeline requires uniform sets which were not set at the time of drawing.");
			ERR_FAIL();
		}

		uint32_t hash_count = dl->validation.pipeline_set_hashes.size();
		const uint32_t *phashes = dl->validation.pipeline_set_hashes.ptr();
		const uint32_t *shashes = dl->validation.set_hashes.ptr();

		for (uint32_t i = 0; i < hash_count; i++) {
			if (phashes[i] == 0) {
				continue; //not used by pipeline, no need to check
			}
			if (phashes[i] != shashes[i]) {
				if (shashes[i] == 0) {
					ERR_EXPLAIN("Uniforms were never supplied for set (" + itos(i) + ") at the time of drawing, which are required by the pipeline");
					ERR_FAIL();
				} else {
					ERR_EXPLAIN("Uniforms supplied for set (" + itos(i) + ") are not the same format as required by the pipeline shader.");
					ERR_FAIL();
				}
			}
		}
	}

	if (p_use_indices) {
		if (!dl->validation.index_array_size) {
			ERR_EXPLAINC("Draw command requested indices, but no index buffer was set.");
			ERR_FAIL();
		}
		if (dl->validation.pipeline_vertex_format != INVALID_ID) {
			//uses vertices, do some vertex validations
			if (dl->validation.vertex_array_size < dl->validation.index_array_max_index) {
				ERR_EXPLAIN("Index array references (max index: " + itos(dl->validation.index_array_max_index) + ") indices beyond the vertex array size (" + itos(dl->validation.vertex_array_size) + ").");
				ERR_FAIL();
			}
		}

		if (dl->validation.pipeline_uses_restart_indices != dl->validation.index_buffer_uses_restart_indices) {
			ERR_EXPLAINC("The usage of restart indices in index buffer does not match the render primitive in the pipeline.");
			ERR_FAIL();
		}

		uint32_t to_draw = dl->validation.index_array_size;

		if (to_draw < dl->validation.pipeline_primitive_minimum) {
			ERR_EXPLAIN("Too few indices (" + itos(to_draw) + ") for the render primitive set in the render pipeline (" + itos(dl->validation.pipeline_primitive_minimum) + ").");
			ERR_FAIL();
		}

		if ((to_draw % dl->validation.pipeline_primitive_divisor) != 0) {
			ERR_EXPLAIN("Index amount (" + itos(to_draw) + ") must be a multiple of the amount of indices required by the render primitive (" + itos(dl->validation.pipeline_primitive_divisor) + ").");
			ERR_FAIL();
		}

		vkCmdDrawIndexed(dl->command_buffer, to_draw, p_instances, dl->validation.index_array_offset, 0, 0);
	} else {
		if (dl->validation.pipeline_vertex_format == INVALID_ID) {
			ERR_EXPLAINC("Draw command lacks indices, but pipeline format does not use vertices.");
			ERR_FAIL();
		}

		uint32_t to_draw = dl->validation.vertex_array_size;

		if (to_draw < dl->validation.pipeline_primitive_minimum) {
			ERR_EXPLAIN("Too few vertices (" + itos(to_draw) + ") for the render primitive set in the render pipeline (" + itos(dl->validation.pipeline_primitive_minimum) + ").");
			ERR_FAIL();
		}

		if ((to_draw % dl->validation.pipeline_primitive_divisor) != 0) {
			ERR_EXPLAIN("Vertex amount (" + itos(to_draw) + ") must be a multiple of the amount of vertices required by the render primitive (" + itos(dl->validation.pipeline_primitive_divisor) + ").");
			ERR_FAIL();
		}

		vkCmdDraw(dl->command_buffer, to_draw, p_instances, 0, 0);
	}
}

void RenderingDeviceVulkan::draw_list_enable_scissor(DrawListID p_list, const Rect2 &p_rect) {
	DrawList *dl = _get_draw_list_ptr(p_list);
	ERR_FAIL_COND(!dl);
	if (!dl->validation.active) {
		ERR_EXPLAIN("Submitted Draw Lists can no longer be modified.");
		ERR_FAIL();
	}

	Rect2i rect = p_rect;
	rect.position += dl->viewport.position;

	rect = dl->viewport.clip(rect);

	if (rect.get_area() == 0) {
		return;
	}
	VkRect2D scissor;
	scissor.offset.x = rect.position.x;
	scissor.offset.y = rect.position.y;
	scissor.extent.width = rect.size.width;
	scissor.extent.height = rect.size.height;

	vkCmdSetScissor(dl->command_buffer, 0, 1, &scissor);
}
