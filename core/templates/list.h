#ifndef LIST_H
#define LIST_H

#include "core/templates/linked_list.h"

// Storage type for a sequence of elements.
// This currently aliases to `LinkedList`, but is not guaranteed to do so in the future.
// If you need a `LinkedList` specifically, please refer to it directly.
// If you need an array-backed list, please use `LocalVector` instead.
template <typename T, typename A = DefaultAllocator>
using List = LinkedList<T, A>;

#endif // LIST_H
